var a = new Date()
var b = a.getDay()
document.write(a + "<br>")

if(b === 0){
    document.write(b + " It is Sunday <br>")
}
else if(b === 1){
    document.write(b + " It is Monday <br>")
}
else if(b === 2){
    document.write(b + " It is Tuesday <br>")
}
else if(b === 3){
    document.write(b + " It is Wednesday <br>")
}
else if(b === 4){
    document.write(b + " It is Thursday <br>")
}
else if(b === 5){
    document.write(b + " It is Friday <br>")
}
else if(b === 6){
    document.write(b + " It is Saturday <br>")
}
